<h1>Välkommen</h1>
<article>
<p class="white">I början av 1970-talet började Ronny Holm, att samla de första sakerna, som nu finns i Begravningsmuseet i Ljungby.
Det var ting som funnits i kyrkor, på kyrkogården, från privatpersoner m fl. Under årens lopp har Ronny Holm handlat i
second handbutiker, varit på loppmarknader och besökt auktioner.I samband med en kyrkogårdsvandring arrangerades en
utställning med föremål från Ronny Holms samling. Flera av deltagarna vid rundvandringen tyckte att utställningen
var så värdefull att den borde permanentas. I samband med Alla Helgons Dag, 2004, invigdes så Sveriges första begravningsmuseum.</p>
<p class="white">
Redan efter ett par veckor hade man haft över 2000 besökare och intresset var mycket stort från både allmänhet,
grupper av alla de slag och inte minst massmedia. Museet visar seder och bruk vid död och begravning under 1800
och 1900-talen. Här visas hela förloppet vid ett dödsfall, från tillkännagivandet via klockringning till
gravsättningen och gravölet. Många av föremålen är skänkta eller utlånade av privatpersoner, institutioner etc.</p>


